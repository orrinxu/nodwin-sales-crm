// One-off generator for the PWA icon set (ORR-705).
// Rasterizes an SVG monogram to the PNG sizes the manifest + iOS need, using the
// seeded brand primary oklch(0.45 0.18 145) converted to an in-gamut sRGB hex so
// the icon, theme_color, and app UI all agree. Re-run if the brand seed changes:
//   node scripts/gen-pwa-icons.mjs
import sharp from "sharp"
import { mkdirSync, writeFileSync } from "node:fs"
import { join } from "node:path"

// ── oklch → sRGB, reducing chroma until the colour fits the sRGB gamut ────────
function oklchToLinearSrgb(L, C, h) {
  const hr = (h * Math.PI) / 180
  const a = C * Math.cos(hr)
  const b = C * Math.sin(hr)
  const l_ = L + 0.3963377774 * a + 0.2158037573 * b
  const m_ = L - 0.1055613458 * a - 0.0638541728 * b
  const s_ = L - 0.0894841775 * a - 1.291485548 * b
  const l = l_ ** 3
  const m = m_ ** 3
  const s = s_ ** 3
  return [
    4.0767416621 * l - 3.3077115913 * m + 0.2309699292 * s,
    -1.2684380046 * l + 2.6097574011 * m - 0.3413193965 * s,
    -0.0041960863 * l - 0.7034186147 * m + 1.707614701 * s,
  ]
}
const inGamut = (rgb) => rgb.every((c) => c >= -1e-4 && c <= 1 + 1e-4)
function encode(c) {
  c = Math.min(1, Math.max(0, c))
  const s = c <= 0.0031308 ? 12.92 * c : 1.055 * c ** (1 / 2.4) - 0.055
  return Math.round(s * 255)
}
function oklchToHex(L, C, h) {
  let lo = 0
  let hi = C
  // binary-search the largest chroma that stays in gamut
  for (let i = 0; i < 40; i++) {
    const mid = (lo + hi) / 2
    if (inGamut(oklchToLinearSrgb(L, mid, h))) lo = mid
    else hi = mid
  }
  const [r, g, b] = oklchToLinearSrgb(L, lo, h).map(encode)
  return `#${[r, g, b].map((v) => v.toString(16).padStart(2, "0")).join("")}`
}

const BRAND = oklchToHex(0.45, 0.18, 145)
console.log("brand primary hex:", BRAND)

// ── SVG monogram ──────────────────────────────────────────────────────────────
// `full` = edge-to-edge (purpose "any"); `safe` keeps the mark inside the maskable
// safe zone (~80% centre) so Android's mask never clips it.
function svg({ size, bg, padded }) {
  const r = padded ? size * 0.5 : size * 0.22 // maskable = circle-safe; any = squircle
  const inset = padded ? size * 0.18 : 0
  const font = padded ? size * 0.44 : size * 0.56
  return `<svg xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}" viewBox="0 0 ${size} ${size}">
  <rect x="${inset}" y="${inset}" width="${size - inset * 2}" height="${size - inset * 2}" rx="${r}" fill="${bg}"/>
  <text x="50%" y="50%" dy="0.02em" text-anchor="middle" dominant-baseline="central"
        font-family="Inter, Arial, sans-serif" font-weight="700" font-size="${font}" fill="#ffffff">N</text>
</svg>`
}

const OUT = join(process.cwd(), "apps/web/public")
mkdirSync(OUT, { recursive: true })

const targets = [
  { file: "icon-192.png", size: 192, padded: false },
  { file: "icon-512.png", size: 512, padded: false },
  { file: "icon-maskable-512.png", size: 512, padded: true },
  { file: "apple-touch-icon.png", size: 180, padded: false }, // iOS: opaque, no transparency
]

for (const t of targets) {
  const buf = Buffer.from(svg({ size: t.size, bg: BRAND, padded: t.padded }))
  await sharp(buf).png().toFile(join(OUT, t.file))
  console.log("wrote", t.file)
}

// Emit the resolved hex so the manifest/layout can import one source of truth.
writeFileSync(
  join(process.cwd(), "apps/web/lib/pwa/brand-color.ts"),
  `// AUTO-GENERATED by scripts/gen-pwa-icons.mjs — the in-gamut sRGB form of the\n// seeded brand primary oklch(0.45 0.18 145). Keep in sync by re-running that script.\nexport const PWA_THEME_COLOR = "${BRAND}"\n`,
)
console.log("wrote apps/web/lib/pwa/brand-color.ts")
